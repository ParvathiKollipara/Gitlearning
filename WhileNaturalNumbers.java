class WhileNaturalNumbers
{
          public static void main(String[] args)
          {
               int start = 1, end = 4, sum = 0;
               while(start<=end)
               {
                  sum = 0;
                  start++;
                  sum = sum+start;
                  System.out.println("Sum is: " +sum);
                }
        }
}
                  
                