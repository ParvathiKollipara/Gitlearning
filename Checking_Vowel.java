class Checking_Vowel
{
   public static void main(String[] args)
   {
    char letter = 'p';
    if(letter=='a' || letter=='e' || letter=='i' || letter=='o' || letter=='u')
    {
      System.out.println(letter+ " is Vowel");
    }
    else
    {
      System.out.println(letter+ " is Consonant");
    }
  }
}