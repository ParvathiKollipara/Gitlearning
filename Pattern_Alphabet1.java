class Pattern_Alphabet1
{
      public static void main(String[] args)
      {
           int rows =4;
           for(int r=0; r<rows; r++)
           {
               for(char ch = 'A'; ch<='D'; ch++)
               {
                    System.out.print(ch+"  ");
                
               }
                 System.out.println();
          }
    }
}                